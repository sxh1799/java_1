package com.itheima.变量;

public class Demo {
    public static void main(String[] args) {
        // 变量的格式 : 数据类型  变量名 = 数据值;
        // 把998数据赋值给了int类型的变量price
        int price = 998;


        System.out.println(price);
        System.out.println(price);
        System.out.println(price);
        System.out.println(price);
        System.out.println(price);
        System.out.println(price);
        System.out.println(price);

        System.out.println("--------------");


        /*
            变量的定义格式 : 数据类型  变量名 = 数据值;
            修改变量 :  变量名 = 新值;
         */
        int age = 18;
        System.out.println(age); // 18
        age = 19;
        System.out.println(age); // 19

        age = age + 1;
        System.out.println(age); // 20


        System.out.println("--------------");

        double height = 180.1;
        System.out.println(height);
    }
}
